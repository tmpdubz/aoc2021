def test_fish_initializer():
  data = read_test_data(test_data)
  fish = initialize_fish(data)
  for i in range(len(fish)):
    assert fish[i].timer == data[i]

def test_fish_iterator():
  data = read_test_data(test_data)
  fish = initialize_fish(data)
  day1 = iterate_fish(fish)
  assert list(map(lambda x: x.timer, day1)) == [2,3,2,0,1]

  day2 = iterate_fish(day1)
  assert list(map(lambda x: x.timer, day1)) == [1,2,1,6,0,8]